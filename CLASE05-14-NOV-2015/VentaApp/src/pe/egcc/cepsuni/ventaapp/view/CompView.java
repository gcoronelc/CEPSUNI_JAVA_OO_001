  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    try {
      // Datos
      String tipo = cboTipo.getSelectedItem().toString();
      String sTotal = txtTotal.getText();
      // Validar
      if(!Validator.esSoloDigitos(sTotal)){
        throw  new NumberFormatException();
      }
      double total = Double.parseDouble(sTotal);
      // Proceso
      ConceptoDto[] repo;
      repo = CompController.procesar(tipo, total);
      // Reporte
      mostrarReporte(repo);
    } catch (NumberFormatException e) {
      JOptionPane.showMessageDialog(rootPane, 
              "El total debe ser un valor num√©rico.",
              "ERROR", JOptionPane.ERROR_MESSAGE);
    }catch (NullPointerException e){
      JOptionPane.showMessageDialog(rootPane, 
              "Debe seleccionar el tipo de comprobante.",
              "ERROR", JOptionPane.ERROR_MESSAGE);
    }
  }//GEN-LAST:event_btnProcesarActionPerformed

